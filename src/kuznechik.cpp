Kuznechik::Kuznechik(const std::vector<uint8_t>& key) {
    if (key.size() != 32) {
        throw std::invalid_argument("Key must be 32 bytes");
    }
    key_schedule(key);
}

uint8_t Kuznechik::gf_mul(uint8_t a, uint8_t b) const {
    uint8_t res = 0;
    while (b) {
        if (b & 1) res ^= a;
        uint8_t carry = a & 0x80;
        a <<= 1;
        if (carry) a ^= 0xC3;
        b >>= 1;
    }
    return res;
}

void Kuznechik::xor_blocks(std::vector<uint8_t>& state, const std::vector<uint8_t>& key) const {
    for (int i = 0; i < 16; i++) {
        state[i] ^= key[i];
    }
}

void Kuznechik::s_transform(std::vector<uint8_t>& state) const {
    for (int i = 0; i < 16; i++) {
        state[i] = S_BOX[state[i]];
    }
}

void Kuznechik::inv_s_transform(std::vector<uint8_t>& state) const {
    for (int i = 0; i < 16; i++) {
        uint8_t val = state[i];
        // Динамический поиск обратного значения по прямому S_BOX
        for (int j = 0; j < 256; j++) {
            if (S_BOX[j] == val) {
                state[i] = static_cast<uint8_t>(j);
                break;
            }
        }
    }
}

void Kuznechik::l_transform(std::vector<uint8_t>& state) const {
    uint8_t k[] = {148, 32, 133, 16, 194, 192, 1, 251, 1, 192, 194, 16, 133, 32, 148, 1};
    for (int step = 0; step < 16; ++step) {
        uint8_t v = 0;
        for (int i = 0; i < 16; ++i) {
            v ^= gf_mul(state[i], k[i]);
        }
        for (int i = 15; i > 0; --i) {
            state[i] = state[i - 1];
        }
        state[0] = v;
    }
}

void Kuznechik::inv_l_transform(std::vector<uint8_t>& state) const {
    uint8_t k[] = {148, 32, 133, 16, 194, 192, 1, 251, 1, 192, 194, 16, 133, 32, 148, 1};
    for (int step = 0; step < 16; ++step) {
        uint8_t v = state[0];
        for (int j = 0; j < 15; ++j) {
            state[j] = state[j + 1];
        }
        for (int j = 0; j < 15; ++j) {
            v ^= gf_mul(state[j], k[j]);
        }
        state[15] = v;
    }
}

void Kuznechik::key_schedule(const std::vector<uint8_t>& key) {
    round_keys.resize(10, std::vector<uint8_t>(16));
    
    std::vector<uint8_t> K1(16), K2(16);
    for (int i = 0; i < 16; i++) {
        K1[i] = key[i];
        K2[i] = key[i + 16];
    }
    
    round_keys[0] = K1;
    round_keys[1] = K2;
    
    for (int i = 1; i <= 4; ++i) {
        std::vector<uint8_t> A = K1;
        std::vector<uint8_t> B = K2;
        
        for (int j = 1; j <= 8; ++j) {
            // Динамическое вычисление константы C_i = L(iter)
            std::vector<uint8_t> C_vec(16, 0);
            C_vec[15] = 8 * (i - 1) + j; 
            l_transform(C_vec);
            
            std::vector<uint8_t> next_B = A; // Сохраняем для смены ветвей сети Фейстеля
            xor_blocks(A, C_vec);
            s_transform(A);
            l_transform(A);
            xor_blocks(A, B);
            B = next_B; // Обязательная смена ветвей
        }
        K1 = A;
        K2 = B;
        round_keys[2 * i] = K1;
        round_keys[2 * i + 1] = K2;
    }
}

std::vector<uint8_t> Kuznechik::encrypt_block(const std::vector<uint8_t>& block) const {
    if (block.size() != 16) {
        throw std::invalid_argument("Block must be 16 bytes");
    }
    
    std::vector<uint8_t> state = block;
    xor_blocks(state, round_keys[0]);
    
    // В Кузнечике нет 10-го раунда без L-слоя (в отличие от AES)
    for (int i = 1; i < 10; ++i) {
        s_transform(state);
        l_transform(state);
        xor_blocks(state, round_keys[i]);
    }
    
    return state;
}

std::vector<uint8_t> Kuznechik::decrypt_block(const std::vector<uint8_t>& block) const {
    if (block.size() != 16) {
        throw std::invalid_argument("Block must be 16 bytes");
    }
    
    std::vector<uint8_t> state = block;
    
    // Обратный порядок применения строго симметричен
    for (int i = 9; i >= 1; --i) {
        xor_blocks(state, round_keys[i]);
        inv_l_transform(state);
        inv_s_transform(state);
    }
    xor_blocks(state, round_keys[0]);
    
    return state;
}

std::vector<uint8_t> Kuznechik::gamma_encrypt(const std::vector<uint8_t>& plaintext, uint64_t iv) const {
    std::vector<uint8_t> ciphertext;
    ciphertext.reserve(plaintext.size());
    
    uint64_t counter = 1;
    
    for (size_t i = 0; i < plaintext.size(); i += 16) {
        std::vector<uint8_t> counter_block(16, 0);
        
        for (int j = 0; j < 8; j++) {
            counter_block[j] = (counter >> (j * 8)) & 0xFF;
        }
        for (int j = 0; j < 8; j++) {
            counter_block[8 + j] = (iv >> (j * 8)) & 0xFF;
        }
        
        std::vector<uint8_t> gamma = encrypt_block(counter_block);
        
        size_t block_size = std::min((size_t)16, plaintext.size() - i);
        for (size_t j = 0; j < block_size; j++) {
            ciphertext.push_back(plaintext[i + j] ^ gamma[j]);
        }
        
        counter++;
    }
    
    return ciphertext;
}

std::vector<uint8_t> Kuznechik::gamma_decrypt(const std::vector<uint8_t>& ciphertext, uint64_t iv) const {
    return gamma_encrypt(ciphertext, iv);
}

std::string bytes_to_hex(const std::vector<uint8_t>& data) {
    std::stringstream ss;
    ss << std::hex << std::setfill('0');
    for (uint8_t byte : data) {
        ss << std::setw(2) << (int)byte;
    }
    return ss.str();
}

std::vector<uint8_t> hex_to_bytes(const std::string& hex) {
    std::vector<uint8_t> bytes;
    std::string h = hex;
    if (h.length() % 2 != 0) {
        h = "0" + h;
    }
    for (size_t i = 0; i < h.length(); i += 2) {
        std::string byte_str = h.substr(i, 2);
        uint8_t byte = static_cast<uint8_t>(std::stoi(byte_str, nullptr, 16));
        bytes.push_back(byte);
    }
    return bytes;
}

